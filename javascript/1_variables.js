
//Naming variables

/*
Variable name 
- cannot contain spaces.
- must begin with a letter, an underscore(_) or a dollar sign($).
- names can only contain letters, numbers, underscores, or dollar signs. 
- names are case-sensitive.
- cannot use reserved keywords. 
*/


var firstName = "Ramkishore";
var age = 21; 
var isFemale = true;
var balance = 100.54;
var dob = new Date(2022, 1, 09);
var person = {};
var empty = undefined;





