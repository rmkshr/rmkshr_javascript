 // Comparison Operators
 // < <= > >= == != 
 // boolean is either true or false
 // the comparison operators returns a boolean after evaluating the condition specified. 

 function comparator (numOne, numTwo) { 
       console.log("Equals : " + (numOne == numTwo)); 
       console.log("Not Equals : " + (numOne != numTwo)); 
       console.log("Less Than : " + (numOne < numTwo)); 
       console.log("Greater Than : " + (numOne > numTwo)); 
       console.log("Less Than Equals : " + (numOne <= numTwo)); 
       console.log("Greater Than Equals : " + (numOne >= numTwo)); 
 }

comparator(10, 10);