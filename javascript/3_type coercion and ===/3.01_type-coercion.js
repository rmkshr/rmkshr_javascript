// Type coercion : It is the process of converting one data type into another in javascript.
// The type coercion is performed automatically or implicitly by the JavaScript engine.
// In computing true is evaluated to 1 and false is evaluated to 0. 
// So in the below statements, we get true when we compare 0 to false.

console.log(0 == false);
console.log(0 == true);
console.log("0" == false);
console.log("0" == true);
console.log("1" == 1);